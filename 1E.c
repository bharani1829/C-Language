#include<stdio.h>
int main()
{
    int a,b;
    float p,q;
    printf("Enter Two Integers : ");
    scanf("%d%d",&a,&b);
    printf("Enter Two Floats : ");
    scanf("%f%f",&p,&q);
    printf("The Sum of Two Integers is %d\n",a+b);
    printf("The Difference of Two Integers is %d\n",a-b);
    printf("The Sum of Two Floats is %f\n",p+q);
    printf("The Difference of Two Floats is %f\n",p-q);
    return 0;




}